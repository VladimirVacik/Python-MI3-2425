# Přidání dvou číselných proměnných
prvniCislo = 20
druheCislo = 10
print(prvniCislo + druheCislo) #výstup součtu čísel

# Kombinace čísel a řetězců
print("Rozdíl čísel je: ", prvniCislo - druheCislo) #výstup rozdílu čísel
# při zřetězení se musí čísla převést na řetězce, tomu se říká přetypování
print("Prvni číslo je " + str(prvniCislo) + " a druhe číslo je " + str(druheCislo)) #výstup čísel pomocí zřetězení

#Složitější příklad
print(f"{prvniCislo} + {druheCislo} = {prvniCislo + druheCislo}") #výstup součtu čísel pomocí interpolace




